import Foundation

/// Breed data for autocomplete in pet registration.
/// Each locale has country-specific breeds in popularity order.
struct Breed: Codable, Identifiable, Hashable {
    let id: String
    let name: String        // English name (stored in DB)
    let species: String
    let alternateNames: [String]?
    let nativeName: String   // Display name in user's locale

    /// Display name: native name for current locale
    var localizedName: String { nativeName }

    enum CodingKeys: String, CodingKey {
        case id, name, species
        case alternateNames = "alternate_names"
        case nativeName = "native_name"
    }

    init(id: String, name: String, species: String, nativeName: String? = nil) {
        self.id = id
        self.name = name
        self.species = species
        self.alternateNames = nil
        self.nativeName = nativeName ?? name
    }
}

/// Per-locale breed data
struct BreedData {
    /// Get breeds for a species using the current device locale
    static func breeds(for species: String) -> [Breed] {
        let lang = Locale.current.language.languageCode?.identifier.lowercased().prefix(2).description ?? "en"
        return breeds(for: species, locale: lang)
    }

    /// Get breeds for a species and specific locale
    static func breeds(for species: String, locale: String) -> [Breed] {
        let lang = locale.lowercased().prefix(2).description
        switch species.lowercased() {
        case "dog":
            return dogBreeds(locale: lang)
        case "cat":
            return catBreeds(locale: lang)
        default:
            return []
        }
    }

    /// Search breeds by name with optional species filter
    static func search(_ query: String, species: String? = nil) -> [Breed] {
        let lowercasedQuery = query.lowercased()
        let breedsToSearch: [Breed]
        if let species = species {
            breedsToSearch = breeds(for: species)
        } else {
            breedsToSearch = breeds(for: "dog") + breeds(for: "cat")
        }
        return breedsToSearch.filter { breed in
            breed.localizedName.lowercased().contains(lowercasedQuery) ||
            breed.name.lowercased().contains(lowercasedQuery)
        }
    }

